const ORDER_STATUS_TASK_MAP: Record<
  Order["status"],
  {
    title: (o: Order) => string;
    column: "today" | "urgent" | "waitingClient";
    tags: string[];
    dueOffsetHours: number;
  } | null
> = {
  Booked: null, // handled by convertLeadToOrder
  Synopsis: {
    title: (o) => `Send Synopsis for ${o.orderId} (${o.clientName})`,
    column: "urgent",
    tags: ["synopsis", "delivery"],
    dueOffsetHours: 24,
  },
  Development: {
    title: (o) => `Progress check: ${o.orderId} — ${o.clientName}`,
    column: "today",
    tags: ["progress", "dev"],
    dueOffsetHours: 72,
  },
  Review: {
    title: (o) => `Get client feedback: ${o.orderId} — ${o.clientName}`,
    column: "waitingClient",
    tags: ["review", "client"],
    dueOffsetHours: 48,
  },
  "Final Payment": {
    title: (o) => `Collect final payment for ${o.orderId} — ${o.clientName}`,
    column: "urgent",
    tags: ["payment", "collection"],
    dueOffsetHours: 24,
  },
  Delivered: {
    title: (o) => `Request testimonial from ${o.clientName} (${o.orderId})`,
    column: "today",
    tags: ["testimonial", "post-delivery"],
    dueOffsetHours: 48,
  },
  Closed: null,
};

/**
 * Fire side-effects when an order status changes.
 */
export const onOrderStatusChange = async (
  order: Order,
  newStatus: Order["status"]
): Promise<void> => {
  const now = Date.now();
  const updates: Partial<Order> = {
    status: newStatus,
    updatedAt: now,
    statusChangedAt: now,
  };

  if (newStatus === "Delivered") {
    updates.deliveredAt = now;
  }

  if (newStatus === "Closed") {
    await logActivity(
      `Order ${order.orderId} closed and archived.`,
      "success",
      "order_status",
      order.id
    );
  }

  const taskSpec = ORDER_STATUS_TASK_MAP[newStatus];
  if (taskSpec) {
    await createAutoTask(taskSpec.title(order), taskSpec.column, taskSpec.tags, {
      linkedOrderId: order.orderId,
      dueAt: now + taskSpec.dueOffsetHours * 60 * 60 * 1000,
    });
    await logActivity(
      `Order ${order.orderId} moved to ${newStatus} — auto-task created.`,
      "new",
      "order_status",
      order.id
    );
  }

  // Compute and save risk flags
  const riskFlags = computeOrderRiskFlags({ ...order, ...updates } as Order);
  updates.riskFlags = riskFlags;

  if (order.id) {
    await updateDoc(doc(db, "orders", order.id), updates as any);
  }
};

