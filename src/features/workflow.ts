/** Generate a short random order ID */
export const generateOrderId = () =>
  `ORD-${Math.floor(1000 + Math.random() * 9000)}`;

/** Create an auto-generated task in Firestore */
export const createAutoTask = async (
  title: string,
  columnId: "today" | "urgent" | "waitingClient" | "done",
  tags: string[],
  options?: {
    linkedOrderId?: string;
    linkedLeadId?: string;
    dueAt?: number; // epoch ms
  }
): Promise<string> => {
  const colTasks = await getDocs(
    query(collection(db, "tasks"), where("columnId", "==", columnId))
  );
  const order = colTasks.size;

  const taskData: Omit<Task, "id"> = {
    title,
    columnId,
    tags,
    createdAt: Date.now(),
    order,
    isAutoGenerated: true,
    ...(options?.linkedOrderId && { linkedOrderId: options.linkedOrderId }),
    ...(options?.linkedLeadId && { linkedLeadId: options.linkedLeadId }),
    ...(options?.dueAt && { dueAt: options.dueAt }),
  };

  const ref = await addDoc(collection(db, "tasks"), taskData);
  return ref.id;
};

/** Compute risk flags for an order */
export const computeOrderRiskFlags = (
  order: Order,
  now: Date = new Date()
): string[] => {
  const flags: string[] = [];

  // Unpaid + deadline within 3 days
  if (order.amount > order.amountPaid && order.deadline) {
    const deadlineDiff =
      (new Date(order.deadline).getTime() - now.getTime()) /
      (1000 * 60 * 60 * 24);
    if (deadlineDiff <= 3 && deadlineDiff >= 0) {
      flags.push("unpaid_near_deadline");
    }
  }

  // No client reply in 5 days
  if (order.lastClientReplyAt) {
    const noReplyDays =
      (now.getTime() - order.lastClientReplyAt) / (1000 * 60 * 60 * 24);
    if (noReplyDays >= 5) {
      flags.push("no_client_reply_5d");
    }
  }

  // Too many status delays (created > 14 days ago, not delivered/closed)
  if (
    order.status !== "Delivered" &&
    order.status !== "Closed" &&
    order.createdAt
  ) {
    const ageDays =
      (now.getTime() - order.createdAt) / (1000 * 60 * 60 * 24);
    if (ageDays > 14) {
      flags.push("status_delay");
    }
  }

  return flags;
};

/** Compute enhanced lead priority score */
export const computeLeadPriorityScore = (lead: Partial<Lead>): number => {
  let score = 50;

  // Source bonus
  if (
    lead.source?.toLowerCase().includes("partner") ||
    lead.source?.toLowerCase().includes("referral") ||
    lead.partnerSource
  )
    score += 20;

  // Budget
  const budgetVal =
    typeof lead.budget === "number"
      ? lead.budget
      : Number(
        (lead.budget as unknown as string)?.replace(/[^0-9]/g, "") || 0
      );
  if (budgetVal > 5000) score += 20;
  else if (budgetVal > 2000) score += 10;
  else if (budgetVal < 500) score -= 10;

  // Recent contact
  if (lead.lastContactedAt) {
    const minutesSince =
      (Date.now() - lead.lastContactedAt) / (1000 * 60);
    if (minutesSince < 60) score += 15; // contacted in last hour
  }

  // Urgent deadline
  if (lead.deadline) {
    const deadlineDays =
      (new Date(lead.deadline).getTime() - Date.now()) / (1000 * 60 * 60 * 24);
    if (deadlineDays <= 3 && deadlineDays >= 0) score += 15;
  }

  // Status penalties
  if (lead.status === "Lost") score -= 30;
  if (lead.status === "Booked") score = 100;
  if (lead.status === "Negotiating") score += 10;

  return Math.min(100, Math.max(0, score));
};

