/**
 * Run all smart reminder checks and create tasks where needed.
 * Call this once per session / on dashboard load.
 */
export const runSmartReminderEngine = async (): Promise<number> => {
  let tasksCreated = 0;
  const now = Date.now();

  try {
    // 1. New lead not contacted in 30 mins
    const leadsSnap = await getDocs(collection(db, "leads"));
    for (const d of leadsSnap.docs) {
      const lead = { id: d.id, ...d.data() } as Lead;
      if (
        lead.status === "New" &&
        now - lead.createdAt > 30 * 60 * 1000
      ) {
        // Check if reminder task already exists
        const existing = await getDocs(
          query(
            collection(db, "tasks"),
            where("linkedLeadId", "==", lead.id),
            where("tags", "array-contains", "reminder-uncontacted")
          )
        );
        if (existing.empty) {
          await createAutoTask(
            `Call new lead: ${lead.name} (${lead.phone || "no phone"})`,
            "urgent",
            ["reminder-uncontacted", "lead", "call"],
            { linkedLeadId: lead.id }
          );
          tasksCreated++;
        }
      }

      // 2. Interested lead idle 24h
      if (
        lead.status === "Interested" &&
        lead.updatedAt &&
        now - lead.updatedAt > 24 * 60 * 60 * 1000
      ) {
        const existing = await getDocs(
          query(
            collection(db, "tasks"),
            where("linkedLeadId", "==", lead.id),
            where("tags", "array-contains", "reminder-interested-idle")
          )
        );
        if (existing.empty) {
          await createAutoTask(
            `Re-engage interested lead: ${lead.name}`,
            "today",
            ["reminder-interested-idle", "follow-up"],
            { linkedLeadId: lead.id }
          );
          tasksCreated++;
        }
      }
    }

    // 3. Payment pending 2 days
    const ordersSnap = await getDocs(collection(db, "orders"));
    for (const d of ordersSnap.docs) {
      const order = { id: d.id, ...d.data() } as Order;
      const twoDaysAgo = now - 2 * 24 * 60 * 60 * 1000;
      if (
        order.amount > order.amountPaid &&
        order.createdAt < twoDaysAgo &&
        order.status !== "Closed" &&
        order.status !== "Delivered"
      ) {
        const existing = await getDocs(
          query(
            collection(db, "tasks"),
            where("linkedOrderId", "==", order.orderId),
            where("tags", "array-contains", "reminder-payment-pending")
          )
        );
        if (existing.empty) {
          await createAutoTask(
            `Collect payment: ${order.orderId} — ${order.clientName} (₹${order.amount - order.amountPaid} pending)`,
            "urgent",
            ["reminder-payment-pending", "payment"],
            { linkedOrderId: order.orderId }
          );
          tasksCreated++;
        }
      }

      // 4. Deadline in 3 days
      if (order.deadline && order.status !== "Delivered" && order.status !== "Closed") {
        const deadlineDiff =
          (new Date(order.deadline).getTime() - now) / (1000 * 60 * 60 * 24);
        if (deadlineDiff <= 3 && deadlineDiff >= 0) {
          const existing = await getDocs(
            query(
              collection(db, "tasks"),
              where("linkedOrderId", "==", order.orderId),
              where("tags", "array-contains", "reminder-deadline")
            )
          );
          if (existing.empty) {
            await createAutoTask(
              `Deadline in ${Math.ceil(deadlineDiff)}d: ${order.orderId} — ${order.topic}`,
              "urgent",
              ["reminder-deadline", "urgent"],
              { linkedOrderId: order.orderId }
            );
            tasksCreated++;
          }
        }
      }
    }

    // 5. Partner inactive 7 days
    const partnersSnap = await getDocs(collection(db, "partners"));
    for (const d of partnersSnap.docs) {
      const partner = { id: d.id, ...d.data() } as Partner;
      const lastActivity = partner.lastLeadAt || partner.createdAt || now;
      const inactiveDays = (now - lastActivity) / (1000 * 60 * 60 * 24);
      if (inactiveDays >= 7) {
        const existing = await getDocs(
          query(
            collection(db, "tasks"),
            where("tags", "array-contains", `reminder-partner-${partner.id}`)
          )
        );
        if (existing.empty) {
          await createAutoTask(
            `Follow up with inactive partner: ${partner.name}`,
            "today",
            [`reminder-partner-${partner.id}`, "partner", "inactive"],
          );
          tasksCreated++;
        }
      }
    }
  } catch (e) {
    console.error("Smart reminder engine error:", e);
  }

  return tasksCreated;
};
