/**
 * Fire side-effects when a lead status changes.
 * Called from leads/page.tsx instead of raw update().
 */
export const onLeadStatusChange = async (
  lead: Lead,
  newStatus: Lead["status"],
  lostReason?: string,
  taskTimings = { followUpHours: 2 }
): Promise<void> => {
  const now = Date.now();
  const updates: Partial<Lead> = {
    status: newStatus,
    updatedAt: now,
    priorityScore: computeLeadPriorityScore({ ...lead, status: newStatus }),
  };

  if (newStatus === "Contacted") {
    updates.lastContactedAt = now;
    await logActivity(
      `${lead.name} marked as Contacted`,
      "active",
      "lead_status",
      lead.id
    );
  }

  if (newStatus === "Interested") {
    // Create follow-up task
    await createAutoTask(
      `Follow-up: ${lead.name} (Interested)`,
      "today",
      ["follow-up", "lead"],
      {
        linkedLeadId: lead.id,
        dueAt: now + taskTimings.followUpHours * 60 * 60 * 1000,
      }
    );
    await logActivity(
      `${lead.name} is Interested — follow-up task created`,
      "new",
      "lead_status",
      lead.id
    );
  }

  if (newStatus === "Negotiating") {
    await logActivity(
      `${lead.name} in Negotiation — suggest sending a quote`,
      "pending",
      "lead_status",
      lead.id
    );
  }

  if (newStatus === "Lost") {
    updates.lostReason = lostReason || "Unknown";
    await logActivity(
      `Lead ${lead.name} marked Lost — reason: ${lostReason || "Unknown"}`,
      "pending",
      "lead_status",
      lead.id
    );
  }

  // Apply updates to Firestore
  if (lead.id) {
    await updateDoc(doc(db, "leads", lead.id), updates as any);
  }
};

/**
 * Convert a lead to an order (Negotiating → Booked flow).
 * Returns the new order ID string.
 */
export const convertLeadToOrder = async (
  lead: Lead
): Promise<string> => {
  const now = Date.now();
  const orderId = generateOrderId();

  // Update lead
  if (lead.id) {
    await updateDoc(doc(db, "leads", lead.id), {
      status: "Booked",
      score: 100,
      priorityScore: 100,
      convertedAt: now,
      updatedAt: now,
    });
  }

  // Create order
  const orderData = {
    orderId,
    leadId: lead.id || "",
    clientName: lead.name,
    college: lead.college,
    topic: "TBD",
    amount:
      typeof lead.budget === "number"
        ? lead.budget
        : Number(
          (lead.budget as unknown as string)?.replace(/[^0-9]/g, "") || 0
        ),
    amountPaid: 0,
    status: "Booked",
    deadline: lead.deadline,
    partnerSource: lead.partnerSource || "",
    createdAt: now,
    updatedAt: now,
    statusChangedAt: now,
    riskFlags: [],
  };

  await addDoc(collection(db, "orders"), orderData);

  // Create onboarding tasks
  const batch = writeBatch(db);
  const onboardingTasks = [
    {
      title: `Create WA Group for ${lead.name}`,
      columnId: "today",
      tags: ["onboarding", "comms"],
    },
    {
      title: `Send Welcome Message to ${lead.name}`,
      columnId: "today",
      tags: ["onboarding", "comms"],
    },
    {
      title: `Collect Advance for ${orderId}`,
      columnId: "urgent",
      tags: ["onboarding", "finance"],
    },
  ];
  onboardingTasks.forEach((t, i) => {
    const ref = doc(collection(db, "tasks"));
    batch.set(ref, {
      ...t,
      id: ref.id,
      createdAt: now,
      order: i,
      isAutoGenerated: true,
      linkedOrderId: orderId,
    });
  });
  await batch.commit();

  // Update partner stats if lead came from a partner
  if (lead.partnerSource) {
    await incrementPartnerConversion(lead.partnerSource);
  }

  await logActivity(
    `Lead ${lead.name} converted to Order #${orderId}. Onboarding started.`,
    "success",
    "conversion",
    lead.id
  );

  return orderId;
};

/** Detect if a phone already exists in leads */
export const checkDuplicatePhone = async (
  phone: string
): Promise<Lead | null> => {
  if (!phone || phone.trim() === "") return null;
  try {
    const q = query(
      collection(db, "leads"),
      where("phone", "==", phone.trim())
    );
    const snap = await getDocs(q);
    if (!snap.empty) {
      return { id: snap.docs[0].id, ...snap.docs[0].data() } as Lead;
    }
  } catch {
    // Ignore errors silently
  }
  return null;
};

