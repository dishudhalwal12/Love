/** Increment partner's leadsSent count when a lead is attributed */
export const incrementPartnerLeadsSent = async (
  partnerName: string
): Promise<void> => {
  const q = query(
    collection(db, "partners"),
    where("name", "==", partnerName)
  );
  const snap = await getDocs(q);
  if (snap.empty) return;
  const partnerDoc = snap.docs[0];
  const partner = partnerDoc.data() as Partner;
  await updateDoc(doc(db, "partners", partnerDoc.id), {
    leadsSent: (partner.leadsSent || 0) + 1,
    lastLeadAt: Date.now(),
    updatedAt: Date.now(),
  });
};

/** Increment partner's converted count + auto-commission calculation */
export const incrementPartnerConversion = async (
  partnerName: string
): Promise<void> => {
  try {
    // Get commission slabs from settings
    const settingsSnap = await getDoc(doc(db, "settings", "global"));
    const settings = settingsSnap.exists() ? settingsSnap.data() : null;
    const commissionSlabs: { minLeads: number; maxLeads: number; percentage: number }[] =
      settings?.commissionSlabs || [{ minLeads: 1, maxLeads: 999, percentage: 10 }];

    const q = query(
      collection(db, "partners"),
      where("name", "==", partnerName)
    );
    const snap = await getDocs(q);
    if (snap.empty) return;

    const partnerDoc = snap.docs[0];
    const partner = partnerDoc.data() as Partner;
    const newConverted = (partner.converted || 0) + 1;

    // Find applicable commission slab
    const slab = commissionSlabs.find(
      (s) => newConverted >= s.minLeads && newConverted <= s.maxLeads
    ) || commissionSlabs[commissionSlabs.length - 1];

    // Commission on the converted lead (estimated ₹1000 base if no order amount available)
    const commissionAmount = (1000 * (slab?.percentage || 10)) / 100;

    await updateDoc(doc(db, "partners", partnerDoc.id), {
      converted: newConverted,
      commissionDue: (partner.commissionDue || 0) + commissionAmount,
      updatedAt: Date.now(),
    });
  } catch (e) {
    console.error("Failed to increment partner conversion:", e);
  }
};

/**
 * Compute inactive days for all partners and update their status.
 * Call this on partner page load.
 */
export const refreshPartnerInactivity = async (): Promise<void> => {
  try {
    const snap = await getDocs(collection(db, "partners"));
    const now = Date.now();
    const batch = writeBatch(db);
    let i = 0;

    snap.docs.forEach((docSnap) => {
      const partner = docSnap.data() as Partner;
      const lastActivity = partner.lastLeadAt || partner.createdAt || now;
      const inactiveDays = Math.floor(
        (now - lastActivity) / (1000 * 60 * 60 * 24)
      );
      const newStatus: Partner["status"] =
        inactiveDays >= 10 ? "Inactive" : "Active";

      batch.update(doc(db, "partners", docSnap.id), {
        inactiveDays,
        status: newStatus,
        updatedAt: now,
      });
      i++;
    });

    if (i > 0) await batch.commit();
  } catch (e) {
    console.error("Failed to refresh partner inactivity:", e);
  }
};

